# Function to create a small change and commit
make_commit() {
    local msg="$1"
    local file_num=$((RANDOM % 10 + 1))
    local change_type=$((RANDOM % 5))
    
    case $change_type in
        0) echo "// Update $RANDOM" >> "temp_file_$file_num.txt" ;;
        1) echo "/* Enhancement $RANDOM */" >> "temp_file_$file_num.js" ;;
        2) echo "# Improvement $RANDOM" >> "temp_file_$file_num.md" ;;
        3) echo "<!-- Change $RANDOM -->" >> "temp_file_$file_num.html" ;;
        4) echo "-- Modification $RANDOM" >> "temp_file_$file_num.sql" ;;
    esac
    
    git add .
    git commit -m "$msg" --quiet
}

# Generate 360+ commits
echo "Generating 360+ commits..."
commit_count=0

for i in {1..20}; do
    for message_template in "${messages[@]}"; do
        for component in "${components[@]}"; do
            if [ $commit_count -ge 360 ]; then
                break 3
            fi
            
            commit_msg=$(printf "$message_template" "$component")
            make_commit "$commit_msg"
            commit_count=$((commit_count + 1))
            
            if [ $((commit_count % 50)) -eq 0 ]; then
                echo "Generated $commit_count commits..."
            fi
        done
    done
done

echo "Generated $commit_count commits total"

# Push to GitHub (assumes remote is already set up)
echo "Pushing to GitHub..."
git push origin main --force

